
public class VariableDeclare1 {

	public static void main(String[] args) {
		int a;//변수선언
		a = 10;//변수초기화
		System.out.println(a);//a변수값출력
		a = 50;//변수값변경
		System.out.println(a);//a변수값출력
		
		int b;
		int c;
		int d,e;
		
		b = 12;
		c = 34;
		d = 67;
		e = 90;
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
		
	}

}




	
