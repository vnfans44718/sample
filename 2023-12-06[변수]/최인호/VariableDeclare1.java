
public class VariableDeclare1 {
	public static void main(String[] args) {
		int a; 	//변수 선언
		a = 10; //변수 초기화
		System.out.println(a);	//a 변수값 출력
		a = 50; //변수값 변경
		System.out.println(a); 	//a 변수값 출력
		
		int b;
		int c;
		int d,e;
		
		b=12;
		c=34;
		d=67;
		e=9011;
		
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		
	}

}
