package com.itwill01.MemberMethod;

public class Calculator {

	public static int add(int x, int y) {
		int sum1 = x + y;
		return sum1;
	}

	public static int sub(int x, int y) {
		int sum1 = x - y;
		return sum1;
	}

	public static int mul(int x, int y) {
		int sum1 = x * y;
		return sum1;
	}

	public static int div(int x, int y) {
		int sum1 = x / y;
		return sum1;
	}

}
