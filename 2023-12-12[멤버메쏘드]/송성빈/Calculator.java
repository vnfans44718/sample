package com.itwill01.method;

public class Calculator {
	
	int add;
	int sub;
	int div;
	int mul;
	
	public int add(int a, int b) {
		return this.add = a + b;	
	}
	
	public int sub(int a, int b) {
		return this.sub = a - b;
	}
	
	public int div(int a, int b) {
		return this.div = a / b;
	}
	
	public int mul(int a, int b) {
		return this.mul = a * b;
	}
}
