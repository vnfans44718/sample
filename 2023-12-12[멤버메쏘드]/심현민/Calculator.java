package com.itwill01.method;

public class Calculator {
	public static int add(int x, int y) {
		int result = x + y;
		return result;
	} 
	public static int sub(int x, int y) {
		int result = x - y;
		return result;
	} 
	public static int div(int x, int y) {
		 int result = x / y;
		 return result;
	} 
	public static int mul(int x, int y) {
		int result = x * y;
		return result;
	} 
}
