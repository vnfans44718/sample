package com.itwill01.method;

public class Calculator {

	int add(int x, int y) {
		int result = x + y;
		return result;
	} 
	int sub(int x, int y) {
		int result = x - y;
		return result;
	} 
	 double div(int x, int y) {
		 double result = (double) x / (double) y;
		 return result;
	} 
	int mul(int x, int y) {
		int result = x * y;
		return result;
	} 

}
