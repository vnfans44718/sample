package com.itwill99.review;

public class MemberMethod {

	public void method1() {
		System.out.println("public void method1() 실행");
	}

	public void method2() {
		System.out.println("public void method2() 실행");
	}
	
	public void method3(int a) {
		System.out.println(a);
	}
	
	public void method4(String b, int c, int d) {
		System.out.println(b+"\t"+c+"\t"+d);
	}
	
	public void method5(String e, String f, int g) {
		System.out.println(e+"\t"+f+"\t"+g);
	}

}
